public class Uygulama_3 {
    public static void main(String[] args) {
        int satirSayisi = 6;
        int sutunSayisi = 10;
        for (int i = 1; i <= satirSayisi; i++){
            for (int j = 1; j <= sutunSayisi; j++){
                System.out.print( i * j + "\t");
            }
            System.out.println();
        }
    }
}
