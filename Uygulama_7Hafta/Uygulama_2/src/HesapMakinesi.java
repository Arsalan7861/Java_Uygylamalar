public class HesapMakinesi {
    public static int topla(int sayi1, int sayi2){
        System.out.println("1. Sayi = " + sayi1 + "\n2. Sayi = " + sayi2);
        return sayi1 + sayi2;
    }

    public static double topla(double sayi1, double sayi2){
        System.out.println("1. Sayi = " + sayi1 + "\n2. Sayi = " + sayi2);
        return sayi1 + sayi2;
    }

    public static int carp(int sayi1, int sayi2){
        System.out.println("1. Sayi = " + sayi1 + "\n2. Sayi = " + sayi2);
        return sayi1 * sayi2;
    }

    public static double carp(double sayi1, double sayi2){
        System.out.println("1. Sayi = " + sayi1 + "\n2. Sayi = " + sayi2);
        return sayi1 * sayi2;
    }

    public static double carp(double sayi1, double sayi2, double sayi3){
        System.out.println("1. Sayi = " + sayi1 + "\n2. Sayi = " + sayi2 + "\n3. Sayi = " + sayi3);
        return sayi1 * sayi2 * sayi3;
    }
}
